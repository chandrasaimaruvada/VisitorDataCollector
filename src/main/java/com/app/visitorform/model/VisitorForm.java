package com.app.visitorform.model;

public class VisitorForm {
    private String name;
    private String mobile;
    private String place;
    private String knowUs;
    private String birthday;
    private String prayerPoints;
    private String submissionDate;
    private String anniversary;
    private String visitPreference;
    private String feedback;

    public String getBirthday() {
        return birthday;
    }

    public void setBirthday(String birthday) {
        this.birthday = birthday;
    }

    public String getKnowUs() {
        return knowUs;
    }

    public void setKnowUs(String knowUs) {
        this.knowUs = knowUs;
    }

    public String getMobile() {
        return mobile;
    }

    public void setMobile(String mobile) {
        this.mobile = mobile;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPlace() {
        return place;
    }

    public void setPlace(String place) {
        this.place = place;
    }

    public String getPrayerPoints() {
        return prayerPoints;
    }

    public void setPrayerPoints(String prayerPoints) {
        this.prayerPoints = prayerPoints;
    }

    public String getSubmissionDate() {
        return submissionDate;
    }

    public void setSubmissionDate(String submissionDate) {
        this.submissionDate = submissionDate;
    }

    public String getAnniversary() {
        return anniversary;
    }

    public void setAnniversary(String anniversary) {
        this.anniversary = anniversary;
    }

    public String getVisitPreference() {
        return visitPreference;
    }

    public void setVisitPreference(String visitPreference) {
        this.visitPreference = visitPreference;
    }

    public String getFeedback() {
        return feedback;
    }

    public void setFeedback(String feedback) {
        this.feedback = feedback;
    }

}
